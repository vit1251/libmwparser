const char *html_entity_to_hex(const char *entity)
{
   if(! htmlmath) {
      entities_init();
      if(! htmlmath)
	 return NULL;
   }

   if(! entity)
      return NULL;

   return eina_hash_find(htmlmath, entity);
}

